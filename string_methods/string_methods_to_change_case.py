str="hello python program"
print("Upper case:",str.upper())
print("Lower case:",str.lower())

a="dahlia, tulip ,HELLO ,WORLD"

print("Upper:",a.upper()) #converts all characters in the string to uppercase(Capital letters).

print("Lower:",a.lower()) #onverts all characters in the string to lowercase(Small letters).

print("Title:",a.title()) #capitalizes the first letter of each word.

print("capitalize:",a.capitalize()) #capitalizes only the first letter of the string.

print("Swapcase:",a.swapcase())  #swaps the uppercase letters in the string to the 
                                  #lowercase; and vice versa.
