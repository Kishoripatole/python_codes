a=[[10,20,30],[40,50,60],[70,80,90]]
print("Nested list:",a)

#accessing elements of a list
print("a_[0]:",a[0])
print("a_[0][0]:",a[0][0])
print("a_[0][1]:",a[0][1])
print("a_[0][2]:",a[0][2])
print("a_[1][0]:",a[1][0])
print("a_[1][1]:",a[1][1])
print("a_[1][2]:",a[1][2])
print("a_[2][0]:",a[2][0])
print("a_[2][1]",a[2][1])
print("a_[2][2]:",a[2][2])